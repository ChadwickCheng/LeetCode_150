// JavaScript 提供了许多用于处理字符串的方法，以下是一些常用的：

// charAt(index): 返回指定位置的字符。
// concat(string2, string3, ..., stringX): 连接两个或多个字符串。
// includes(searchvalue, start): 检查一个字符串是否包含在另一个字符串中，返回 true 或 false。
// indexOf(searchvalue, start): 返回某个指定的字符串值在字符串中首次出现的位置。
// lastIndexOf(searchvalue, start): 返回一个指定的字符串值最后出现的位置，在一个字符串中的指定位置从后向前搜索。
// replace(searchvalue, newvalue): 在字符串中替换一些字符。
// slice(start, end): 提取字符串的一部分，并在新的字符串中返回被提取的部分。
// split(separator, limit): 把字符串分割为字符串数组。
// substring(start, end): 提取字符串中两个指定的索引号之间的字符。
// toLowerCase(): 把字符串转换为小写。
// toUpperCase(): 把字符串转换为大写。
// trim(): 删除字符串的首尾空格。

// 这些方法可以帮助你在 JavaScript 中更有效地处理字符串。