/*
分治（Divide and Conquer）是一种解决问题的策略，它将一个复杂的问题分解成两个或更多的相同或相似的子问题，直到最后子问题可以简单的直接求解，原问题的解即子问题的解的合并。

使用分治的原因：

1. 分治法将问题分解为独立的子问题，这样做的好处是子问题通常比原问题更容易解决。
2. 分治法可以大大提高算法的效率。例如，归并排序和快速排序算法就是使用分治策略的典型例子，它们的时间复杂度都可以达到 O(n log n)。
3. 分治法可以并行化，子问题通常可以在不同的处理器或机器上并行解决。
如何使用分治：

1. 分解：将原问题分解为一系列子问题。
2. 解决：如果子问题足够小，那么直接解决。如果子问题还是太复杂，那么再次应用分治策略。
3. 合并：将子问题的结果合并为原问题的解。
在实际编程中，分治策略通常通过递归实现。
*/